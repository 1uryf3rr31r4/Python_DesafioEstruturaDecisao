diaSemana = int(input("Digite o dia da semana 1-Domingo, 2-Segunda, 3-Terça, etc: "))

if diaSemana == 1:
    print("dia de Domingo")
elif diaSemana == 2:
    print("dia de Segunda-feira")
elif diaSemana == 3:
    print("dia de Terça-feira")
elif diaSemana == 4:
    print("dia de Quarta-feira")
elif diaSemana == 5:
    print("dia de Quinta-feira")
elif diaSemana == 6:
    print("dia de Sexta-feira")
elif diaSemana == 7:
    print("dia de Sábado")
else:
    print("Valor Inválido")
    
